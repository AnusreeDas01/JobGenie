# PDF Export with fpdf (Streamlit compatible)
from fpdf import FPDF
from datetime import datetime
import base64
import os

class PDF(FPDF):
    def header(self):
        self.set_font("Arial", "B", 16)
        self.set_text_color(40, 40, 40)
        self.cell(0, 10, "‚ú® JobGenie - Resume Feedback Report ‚ú®", ln=1, align="C")

    def add_section(self, title, content, bullet=False):
        self.set_font("Arial", "B", 14)
        self.set_text_color(0, 102, 204)
        self.cell(0, 10, f"{title}", ln=1)

        self.set_font("Arial", "", 11)
        self.set_text_color(50, 50, 50)

        if isinstance(content, list):
            for item in content:
                prefix = "‚Ä¢ " if bullet else ""
                self.multi_cell(0, 8, f"{prefix}{item}")
        else:
            self.multi_cell(0, 8, str(content))

        self.ln(4)

def export_pdf_with_style(score, matched, total, matched_keywords, missing_keywords, summary, skill_gap, suggestions, rewritten):
    pdf = PDF()
    pdf.add_page()

    pdf.set_auto_page_break(auto=True, margin=15)

    # Sections
    pdf.add_section("üìä Match Score", f"{score}% ({matched}/{total} keywords matched)")
    pdf.add_section("‚úÖ Matched Skills", matched_keywords, bullet=True)
    pdf.add_section("‚ùå Missing Skills", missing_keywords, bullet=True)
    pdf.add_section("üìù Career Summary", summary)
    pdf.add_section("üîç Skills Gap", skill_gap, bullet=True)
    pdf.add_section("üí° Suggestions to Improve Resume", suggestions, bullet=True)
    pdf.add_section("‚ú® Rewritten Resume Bullet", rewritten)

    # Footer
    pdf.set_y(-30)
    pdf.set_font("Arial", "I", 10)
    pdf.set_text_color(120, 120, 120)
    pdf.cell(0, 10, f"Generated by JobGenie | jobgenie.ai | {datetime.now().strftime('%d %b %Y')}", 0, 0, "C")

    # Save to file
    filename = "JobGenie_Styled_Report.pdf"
    pdf.output(filename)

    # Convert to base64 for download link
    with open(filename, "rb") as f:
        b64 = base64.b64encode(f.read()).decode()

    href = f'<a href="data:application/pdf;base64,{b64}" download="{filename}">üì• Download PDF Report</a>'
    return href



